class Switch_01{
	public static void main(String args[]){
	int x=2;
	// int y=10;
		switch(x){
		case 1:
		System.out.println("Hello");
		break;
		case 2:
		System.out.println("Hi");
		break;
                case 3:
                System.out.println("Bye");
		break;
                default:
                System.out.println("Namaskar");
		break;
		}
	}
}
