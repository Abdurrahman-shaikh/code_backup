class Switch_02{
	public static void main(String args[]){
	int x=66;
		switch(x){
		case 97:
		System.out.println("97");
		break;
                case 'B':
                System.out.println("B");
		break;
                case 65:
                System.out.println("65");
		break;
                default:
                System.out.println("default");
		}
	}
}
